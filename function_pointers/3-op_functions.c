#include "3-calc.h"

/**
 * op_add - function that add
 * @a: first var
 * @b: second var
 * Return: a add by b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function that substract
 * @a: first var
 * @b: second var
 * Return: a sub by b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function that multiplicates
 * @a: first var
 * @b: second var
 * Return: a multiplicate by b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function that divid
 * @a: first var
 * @b: second var
 * Return: a divided by b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function that modulo
 * @a: first var
 * @b: second var
 * Return: a modulo by b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
